    private void actualizarArray() {
        int indexD = 0, indexC = 0;

        for (int i = 0; i < listPersonas.size(); i++) {
            if (doctor.getUsuario().equals(listPersonas.get(i).getUsuario())) {
                indexD = i;
            }
        }

        for (int i = 0; i < listPersonas.size(); i++) {
            if (cliente.getUsuario().equals(listPersonas.get(i).getUsuario())) {
                indexC = i;
            }
        }

        listPersonas.set(indexC, cliente);
        listPersonas.set(indexD, doctor);
    }

    public ArrayList<Personaa> getListPersonas() {
        return listPersonas;
    }

