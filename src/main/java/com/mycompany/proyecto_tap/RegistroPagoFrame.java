                            if (nuevoPago == doctor.getCostoConsulta()) {//checamos si ya se saldó por completo la consulta
                                System.out.println("Ya se pagó por completo");
                                cita.setCitaPagada(true);
                                System.out.println("entró a pago completado");
                                cita.setAvancePago(doctor.getCostoConsulta());
                                System.out.println("guardó pago completo");
                                textF = false;
                            } else {
                                System.out.println("Se abonó");
                                cita.setAvancePago(nuevoPago);
                            }
                            //citasReservadas.set(indexCD, cita);
                            //System.out.println("doc cool");
                            //cliente.getCitasReservadas().set(indexCC, cita);
                            //System.out.println("cliente cool");
